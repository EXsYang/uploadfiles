#演示decimal类型、float、double使用
# decimal[M,D] M是小数位数(精度)的总和，D是小数点(标度)后面的位数
# 如果D是0，则值没有小数点或分数部分。M最大65，D最大30。如果M被省略，默认是10，如果D被省略，默认是0

#创建表
